@dataclass(frozen=True)
class SourceEntry:
    id: str
    title: str
    method: str
    class_name: str
    qualified_name: str
    source_file: str
    line: int | None
    is_deprecated: bool
    raw_javadoc: str
    category: str


@dataclass(frozen=True)
class BatchJob:
    index: int
    category: str
    entries: list[SourceEntry]


@dataclass(frozen=True)
class BatchResult:
    job: BatchJob
    output: dict[str, dict[str, Any]]
    warnings: list[str]
    error: str | None = None


